public class FaceModel {
    private String name;
    private String file;
    private String mrf;
    private int id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFile() {
        return file;
    }

    public void setFile(String file) {
        this.file = file;
    }

    public String getMrf() {
        return mrf;
    }

    public void setMrf(String mrf) {
        this.mrf = mrf;
    }
}
